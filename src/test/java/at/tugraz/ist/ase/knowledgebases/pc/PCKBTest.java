    @Test
    void testConstraints() {
        List<Constraint> constraintList = kb.getConstraintList();

        assertEquals(expectedConstraints.size(), constraintList.size());

        for (int i = 0; i < constraintList.size(); i++) {
            List<String> expectedConstraintList = expectedConstraints.get(i);
            List<String> expectedNegConstraintList = expectedNegConstraints.get(i);
            List<org.chocosolver.solver.constraints.Constraint> actualConstraints = constraintList.get(i).getChocoConstraints();
            List<org.chocosolver.solver.constraints.Constraint> actualNegConstraints = constraintList.get(i).getNegChocoConstraints();

            assertEquals(expectedConstraintList.size(), actualConstraints.size(), "Constraint " + i + " has wrong number of constraints");

            for (int j = 0; j < expectedConstraintList.size(); j++) {
                assertEquals(expectedConstraintList.get(j), actualConstraints.get(j).toString(), "Constraint " + i + " has wrong constraint " + j);
            }

            for (int j = 0; j < expectedNegConstraintList.size(); j++) {
                assertEquals(expectedNegConstraintList.get(j), actualNegConstraints.get(j).toString(), "Constraint " + i + " has wrong constraint " + j);
            }
        }
    }

